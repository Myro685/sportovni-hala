// Function to change language
function changeLanguage(lang) {
  document.documentElement.lang = lang;

  document.querySelectorAll("[data-lang-key]").forEach((element) => {
    const key = element.getAttribute("data-lang-key");
    if (
      (element.tagName === "INPUT" || element.tagName === "TEXTAREA") &&
      element.hasAttribute("placeholder")
    ) {
      element.setAttribute("placeholder", translations[lang][key]);
    } else {
      element.textContent = translations[lang][key];
    }
  });

  
  document.querySelectorAll(".language-btn").forEach((button) => {
    const buttonLang = button.getAttribute("data-lang");
    if (buttonLang === lang) {
      button.classList.remove("grayscale-[0.9]");
    } else {
      button.classList.add("grayscale-[0.9]");
    }
  });

  localStorage.setItem("preferredLanguage", lang); // Save preference to localStorage
}

// Initialization on  load
document.addEventListener("DOMContentLoaded", () => {
  // Adding event listeners to language buttons
  document.querySelectorAll(".language-btn").forEach((button) => {
    button.addEventListener("click", () => {
      const lang = button.getAttribute("data-lang");
      changeLanguage(lang);
    });
  });

  // Load saved language or default to "cs"
  const savedLang = localStorage.getItem("preferredLanguage") || "cs";
  changeLanguage(savedLang);
});
